
public interface Movable {
	
	
	public double getCenterX(); //Get the center X pos of the object in relation to the enclosing container
	
	public double getCenterY(); //Get the center Y position of the object in relation to the enclosing container.
	
	public void move(); //Move the object on the screen.
	
	public double getBoundingRadius(); //Get the radius of the bounding circle around the object.
	
	
	
} //end Moveable
